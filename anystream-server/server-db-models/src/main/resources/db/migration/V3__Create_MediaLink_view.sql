CREATE VIEW mediaLinkView AS
SELECT mediaLink.id                  mediaLink_id,
       mediaLink.gid                 mediaLink_gid,
       mediaLink.metadataId          mediaLink_metadataId,
       mediaLink.metadataGid         mediaLink_metadataGid,
       mediaLink.rootMetadataId      mediaLink_rootMetadataId,
       mediaLink.rootMetadataGid     mediaLink_rootMetadataGid,
       mediaLink.parentMediaLinkId   mediaLink_parentMediaLinkId,
       mediaLink.parentMediaLinkGid  mediaLink_parentMediaLinkGid,
       mediaLink.addedAt             mediaLink_addedAt,
       mediaLink.updatedAt           mediaLink_updatedAt,
       mediaLink.addedByUserId       mediaLink_addedByUserId,
       mediaLink.mediaKind           mediaLink_mediaKind,
       mediaLink.type                mediaLink_type,
       mediaLink.filePath            mediaLink_filePath,
       mediaLink.directory           mediaLink_directory,
       mediaLink.fileIndex           mediaLink_fileIndex,
       mediaLink.hash                mediaLink_hash,
       mediaLink.descriptor          mediaLink_descriptor,
       streamEncoding.id             streamEncoding_id,
       streamEncoding.streamId       streamEncoding_streamId,
       streamEncoding.codecName      streamEncoding_codecName,
       streamEncoding.codecLongName  streamEncoding_codecLongName,
       streamEncoding.'index'        streamEncoding_index,
       streamEncoding.language       streamEncoding_language,
       streamEncoding.profile        streamEncoding_profile,
       streamEncoding.bitRate        streamEncoding_bitRate,
       streamEncoding.channels       streamEncoding_channels,
       streamEncoding.channelLayout  streamEncoding_channelLayout,
       streamEncoding.level          streamEncoding_level,
       streamEncoding.height         streamEncoding_height,
       streamEncoding.width          streamEncoding_width,
       streamEncoding.type           streamEncoding_type,
       streamEncoding.title          streamEncoding_title,
       streamEncoding.colorSpace     streamEncoding_colorSpace,
       streamEncoding.colorRange     streamEncoding_colorRange,
       streamEncoding.colorTransfer  streamEncoding_colorTransfer,
       streamEncoding.colorPrimaries streamEncoding_colorPrimaries,
       streamEncoding.pixFmt         streamEncoding_pixFmt,
       streamEncoding.fieldOrder     streamEncoding_fieldOrder,
       streamEncoding.sampleFmt      streamEncoding_sampleFmt,
       streamEncoding.sampleRate     streamEncoding_sampleRate,
       streamEncoding.duration       streamEncoding_duration,
       streamEncoding.mediaLinkId    streamEncoding_mediaLinkId,
       streamEncoding.'default'      streamEncoding_default
FROM mediaLink
         LEFT JOIN streamEncoding on streamEncoding.mediaLinkId = mediaLink.id;
